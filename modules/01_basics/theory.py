# for + range
print("Таблица умножения на 3:")
for i in range(1, 11):
    print(f"3 × {i:2} = {3 * i}")

# while с break
print("\nИщем первое число, делящееся на 7 и на 11:")
n = 1
while True:
    if n % 7 == 0 and n % 11 == 0:
        print(f"Нашли: {n}")
        break
    n += 1

# continue — пропуск итерации
print("\nЧётные числа от 1 до 20:")
for i in range(1, 21):
    if i % 2 != 0:
        continue
    print(i, end=" ")
print()  # перевод строки


