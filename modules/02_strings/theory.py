# ─────────────────────────────────────────────
# 1. Основы строк
# ─────────────────────────────────────────────
print("=" * 40)
print("1. String basics")
print("=" * 40)

s = "Hello, Python!"
print(f"Строка:        {s}")
print(f"Длина:         {len(s)}")    # количество символов
print(f"Первый символ: {s[0]}")      # индексация с нуля
print(f"Последний:     {s[-1]}")     # отрицательный индекс — с конца


# ─────────────────────────────────────────────
# 2. Срезы (slicing)
# ─────────────────────────────────────────────
print("\n" + "=" * 40)
print("2. Slicing")
print("=" * 40)

s = "Python"
#    012345   <- прямые индексы
#   -6-5-4-3-2-1 <- отрицательные индексы

print(f"s[0:3]  = {s[0:3]}")    # символы с 0 по 2 включительно
print(f"s[2:]   = {s[2:]}")     # с индекса 2 до конца
print(f"s[:4]   = {s[:4]}")     # с начала до индекса 3
print(f"s[-3:]  = {s[-3:]}")    # последние 3 символа
print(f"s[::2]  = {s[::2]}")    # каждый второй символ
print(f"s[::-1] = {s[::-1]}")   # шаг -1 разворачивает строку


# ─────────────────────────────────────────────
# 3. Основные методы строк
# ─────────────────────────────────────────────
print("\n" + "=" * 40)
print("3. String methods")
print("=" * 40)

s = "  Hello, World!  "

print(s.upper())                         # всё в верхний регистр
print(s.lower())                         # всё в нижний регистр
print(s.strip())                         # убрать пробелы по краям
print(s.strip().replace("World", "Python"))  # заменить подстроку
print(s.strip().find("World"))           # индекс первого вхождения, -1 если нет
print(s.strip().count("l"))              # количество вхождений
print(s.strip().startswith("He"))        # начинается с "He"?
print(s.strip().endswith("!"))           # заканчивается на "!"?


# ─────────────────────────────────────────────
# 4. split и join
# ─────────────────────────────────────────────
print("\n" + "=" * 40)
print("4. Split and join")
print("=" * 40)

csv = "apple,banana,orange"
fruits = csv.split(",")    # разбиваем строку по разделителю → список
print(fruits)

words = "one two three".split()  # без аргумента — разбивает по пробелам
print(words)

print(" | ".join(fruits))        # склеиваем список обратно в строку
print("".join(["a", "b", "c"]))  # без разделителя


# ─────────────────────────────────────────────
# 5. Форматирование f-строк
# ─────────────────────────────────────────────
print("\n" + "=" * 40)
print("5. f-string formatting")
print("=" * 40)

name = "Alice"
score = 95.5678

print(f"Player: {name}")
print(f"Score: {score:.2f}")          # 2 знака после точки
print(f"Score: {score:08.2f}")        # 8 символов, заполнить нулями
print(f"{'Title':^30}")               # центрировать в 30 символах
print(f"{'Left':<20}|{'Right':>20}")  # выравнивание влево и вправо


# ─────────────────────────────────────────────
# 6. Строки — это последовательности
# ─────────────────────────────────────────────
print("\n" + "=" * 40)
print("6. Strings as sequences")
print("=" * 40)

s = "Python"

print("Py" in s)   # True — проверка вхождения подстроки
print("py" in s)   # False — регистр важен!

# перебор символов как в обычном списке
for char in "ABC":
    print(char, end=" ")
print()

# enumerate даёт пару (индекс, символ)
for i, char in enumerate("Hi!"):
    print(f"  index {i} -> '{char}'")